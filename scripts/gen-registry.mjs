#!/usr/bin/env node
/**
 * Auto-discovery registry generator.
 *
 * Scans src/reviz/components for component modules that export both a default
 * React component and a named `meta`, then writes src/reviz/registry.generated.ts.
 *
 * This decoupling is deliberate: dozens of components can be authored in
 * parallel (each its own file) without ever touching a shared registry file, so
 * there are zero merge conflicts. Run via `yarn gen:registry`.
 */
import { readdirSync, readFileSync, writeFileSync, statSync } from "node:fs";
import { join, relative, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const COMPONENTS_DIR = join(ROOT, "src", "reviz", "components");
const OUT = join(ROOT, "src", "reviz", "registry.generated.ts");

function walk(dir) {
  let files = [];
  let entries = [];
  try {
    entries = readdirSync(dir);
  } catch {
    return files;
  }
  for (const name of entries) {
    const full = join(dir, name);
    const st = statSync(full);
    if (st.isDirectory()) {
      files = files.concat(walk(full));
    } else if (name.endsWith(".tsx") && !name.startsWith("_") && name !== "index.tsx") {
      files.push(full);
    }
  }
  return files;
}

const files = walk(COMPONENTS_DIR).sort();
const modules = [];

for (const file of files) {
  const src = readFileSync(file, "utf8");
  const hasMeta = /export\s+const\s+meta\b/.test(src);
  const hasDefault = /export\s+default\b/.test(src);
  if (!hasMeta || !hasDefault) continue;
  // import path relative to src/reviz, posix-style, no extension
  let rel = relative(join(ROOT, "src", "reviz"), file).replace(/\\/g, "/").replace(/\.tsx$/, "");
  if (!rel.startsWith(".")) rel = "./" + rel;
  modules.push(rel);
}

const importLines = modules
  .map((m, i) => `import { meta as meta_${i}, default as Comp_${i} } from "${m}";`)
  .join("\n");

const entryLines = modules
  .map((_, i) => `  { meta: meta_${i}, Component: Comp_${i} as unknown as RevizComponent },`)
  .join("\n");

const out = `// AUTO-GENERATED by scripts/gen-registry.mjs — do not edit by hand.
// Run \`yarn gen:registry\` to regenerate.
/* eslint-disable */
import type { RegistryEntry, RevizComponent } from "./types";

${importLines}

export const REGISTRY: RegistryEntry[] = [
${entryLines}
];
`;

writeFileSync(OUT, out, "utf8");
console.log(`[reviz] registry: ${modules.length} components -> ${relative(ROOT, OUT)}`);
